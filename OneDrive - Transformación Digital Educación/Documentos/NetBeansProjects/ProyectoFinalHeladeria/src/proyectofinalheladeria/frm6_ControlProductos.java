    public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            Producto.addColumn("Codigo_producto");
            Producto.addColumn("Producto");
            Producto.addColumn("Precio");
            Producto.addColumn("Disponibilidad");

            this.TablaProducto.setModel(Producto);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM `Producto`";
            }
            else{
            sql="SELECT * FROM `Producto` WHERE `Codigo_producto`='"+valor+"'";
            } 

            String []datos=new String [6];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);

            Producto.addRow(datos);
            }
            TablaProducto.setModel(Producto);
            }catch(SQLException ex){
            Logger.getLogger(frm6_ControlProductos.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    
     public void RefrescarTabla(){
        //Funcion encargada de Refrescar la tabla utilizando Revalidate
        try {
        Producto.setColumnCount(0);
        Producto.setRowCount(0);
        TablaProducto.revalidate();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }
    
