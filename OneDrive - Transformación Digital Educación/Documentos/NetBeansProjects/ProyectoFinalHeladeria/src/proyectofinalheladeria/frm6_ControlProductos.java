    private void jbInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInsertarActionPerformed
        // TODO add your handling code here:
        String cod = jtCodigo.getText();
        String pro = jtProducto.getText();
        String pre = jtPrecio.getText();
        String can = jtDisponibilidad.getText();
        
        PreparedStatement ps;
        String query = "INSERT INTO `Producto`(`Codigo_producto`,`Producto`,`Precio`,`Disponibilidad`) VALUES (?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            //            ps.setString(1, id);
            ps.setString(1, cod);
            ps.setString(2, pro);
            ps.setString(3, pre);
            ps.setString(4, can);

            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo dato ingresado");
                Limpiar();
                Mostrardatos("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(frm6_ControlProductos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_jbInsertarActionPerformed

