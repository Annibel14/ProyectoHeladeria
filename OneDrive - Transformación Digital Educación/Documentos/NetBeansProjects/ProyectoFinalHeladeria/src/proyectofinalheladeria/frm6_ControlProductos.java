    private void Enviar(){
       
        String Cod = jtCodigo.getText();
        String Dis = jtDisponibilidad.getText();
        
       PreparedStatement ps;
        String query = "UPDATE `Producto` SET `Disponibilidad`= Disponibilidad - "+Dis+" WHERE `Codigo_producto`=" + Cod;
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Producto Vendido");
                Limpiar();
                Mostrardatos("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(frm6_ControlProductos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }
    
