    public final void Mostrardatos(String valor){
            //Funcion para llenar la tabla de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            UsuarioF.addColumn("Id");
            UsuarioF.addColumn("Nombre");
            UsuarioF.addColumn("NombreUsuario");
            UsuarioF.addColumn("Correo");
            UsuarioF.addColumn("Contraseña");

            this.TablaUsuario.setModel(UsuarioF);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM `UsuarioF`";
            }
            else{
            sql="SELECT * FROM `UsuarioF` WHERE `Id`='"+valor+"'";
            } 

            String []datos=new String [6];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            datos[4]=rs.getString(5);

            UsuarioF.addRow(datos);
            }
            TablaUsuario.setModel(UsuarioF);
            }catch(SQLException ex){
            Logger.getLogger(frm3_ControlUsuarios.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
    }

