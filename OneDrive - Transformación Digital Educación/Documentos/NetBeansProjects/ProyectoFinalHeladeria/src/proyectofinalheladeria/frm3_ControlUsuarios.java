    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        // TODO add your handling code here: 
//        Funcion para actualizar la tabla Usuarios desde los TextField con el boton
                
        String id = jtId.getText();
        String nom = jtNombre.getText();
        String usu = jtUsuario.getText();
        String cor = jtCorreo.getText();
        String con = jpContraseña.getText();

        PreparedStatement ps;
        String query = "UPDATE `UsuarioF` SET `Id`=?, `NombreUsuario`=?, `Correo`=?, `Contraseña`=? WHERE `Nombre`=?";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(1, nom);
            ps.setString(5, id);
            ps.setString(2, usu);
            ps.setString(3, cor);
            ps.setString(4, con);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, " Usuario Modificado");
            Limpiar();
            Mostrardatos("");

        } catch (SQLException ex) {
            Logger.getLogger(frm3_ControlUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_jbActualizarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here: 
//        Funcion para eliminar datos desde los TextField con el boton
                
        String id = (String)UsuarioF.getValueAt(TablaUsuario.getSelectedRow(),0);

        PreparedStatement ps;
        String query = "DELETE FROM `UsuarioF` WHERE `Id`=?";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(1, id);

            if(ps.executeUpdate() > 0)
            {

                JOptionPane.showMessageDialog(null, "Usuario Eliminado");
                Limpiar();
                Mostrardatos("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(frm3_ControlUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    public void RefrescarTabla(){
        //Funcion encargada de Refrescar la tabla utilizando Revalidate
        try {
        UsuarioF.setColumnCount(0);
        UsuarioF.setRowCount(0);
        TablaUsuario.revalidate();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }
    
