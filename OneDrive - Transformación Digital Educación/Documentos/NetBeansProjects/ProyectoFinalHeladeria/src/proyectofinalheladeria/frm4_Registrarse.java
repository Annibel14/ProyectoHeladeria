    private void jbEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEnviarActionPerformed
        // Codigopara guaradar los datos de empleado cliente en la base de datos
        String nom = jtNombre.getText();
        String usu = jtUsuario.getText();
        String correo = jtCorreo.getText();
        String cont = jpContraseña.getText();
        String roles = cbRoles.getItemAt(cbRoles.getSelectedIndex());
        
//        int rroles = Integer.parseInt(roles);
        
        PreparedStatement ps;
        String query = "INSERT INTO `UsuarioF`(`Nombre`,`NombreUsuario`,`Correo`,`Contraseña`,`Id_rol`) VALUES (?,?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, nom);
            ps.setString(2, usu);
            ps.setString(3, correo);
            ps.setString(4, cont);
            ps.setString(5, roles);
            
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo dato ingresado");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(frm4_Registrarse.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        
        frm2_InicioSesion mf = new frm2_InicioSesion();
        mf.setVisible(true);
        mf.pack();
        mf.setLocationRelativeTo(null);
        
        this.dispose();
        
        
    }//GEN-LAST:event_jbEnviarActionPerformed

