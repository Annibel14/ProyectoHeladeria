    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        String nofac = tfFactura.getText();
        String fecha = tfFechaF.getText();
        String hora = tfHora.getText();
        String caj = tfCajera.getText();
        String nomcli = tfNombreC.getText();
        String forpag = tfFormaPago.getText();
        String pro = tfProducto.getText();
        String sab = tfSabor.getText();
        String can = tfCantidad.getText();
        String total = tfTotal.getText();
        
        PreparedStatement ps;
        String query = "INSERT INTO `Facturacion`(`NoFactura`,`FechaF`,`HoraF`,`Cajera`,`NombreCliente`,`FormaPago`,`Producto`,`Sabor`,`Cantidad`,`Total`) VALUES (?,?,?,?,?,?,?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
//            ps.setString(1, id);
            ps.setString(1, nofac);
            ps.setString(2, fecha);
            ps.setString(3, hora);
            ps.setString(4, caj);
            ps.setString(5, nomcli);
            ps.setString(6, forpag);
            ps.setString(7, pro);
            ps.setString(8, sab);
            ps.setString(9, can);
            ps.setString(10, total);
            
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Factura Guardada");
                Limpiar();
                Mostrardatos("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TablaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
    }//GEN-LAST:event_jbGuardarActionPerformed

