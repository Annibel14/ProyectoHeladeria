    private void jbImprimirFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbImprimirFActionPerformed
        // TODO add your handling code here:
        Connection cn=MyConnection.getConnection();
        
        try{
            JasperReport jr = (JasperReport) JRLoader.loadObject(Factura.class.getResource("/Reporte/Facturacion.jasper"));
            Map parametros = new HashMap<>();
            parametros.put("Titulo", "Reporte Factura");
            

           JasperPrint jp = JasperFillManager.fillReport(jr, parametros, cn);
           JasperViewer jv = new JasperViewer(jp, false);
           jv.setVisible(true); 
        
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_jbImprimirFActionPerformed

    public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            Factura.addColumn("NoFactura");
            Factura.addColumn("FechaF");
            Factura.addColumn("HoraF");
            Factura.addColumn("Cajera");
            Factura.addColumn("NombreCliente");
            Factura.addColumn("FormaPago");
            Factura.addColumn("Producto");
            Factura.addColumn("Sabor");
            Factura.addColumn("Cantidad");
            Factura.addColumn("Total");

            this.TablaFactura.setModel(Factura);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM `Facturacion`";
            }
            else{
            sql="SELECT * FROM `Facturacion` WHERE `NoFactura`='"+valor+"'";
            } 

            String []datos=new String [10];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            datos[4]=rs.getString(5);
            datos[5]=rs.getString(6);
            datos[6]=rs.getString(7);
            datos[7]=rs.getString(8);
            datos[8]=rs.getString(9);
            datos[9]=rs.getString(10);

            Factura.addRow(datos);
            }
            TablaFactura.setModel(Factura);
            }catch(SQLException ex){
            Logger.getLogger(Factura.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    
